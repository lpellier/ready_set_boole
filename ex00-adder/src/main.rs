mod adder;

use adder::adder::adder;

fn main() {
    println!("{0} + {1} = {2} ({3})", 25, 12, adder(25, 12), (25 + 12));
    println!("{0} + {1} = {2} ({3})", 56, 23, adder(56, 23), (56 + 23));
    println!("{0} + {1} = {2} ({3})", 56440, 56440, adder(56440, 56440), (56440 + 56440));
    println!("{0} + {1} = {2} ({3})", 5874440, 6451, adder(5874440, 6451), (5874440 + 6451));
    println!("{0} + {1} = {2} ({3})", 9, 2, adder(9, 2), (9 + 2));
    println!("{0} + {1} = {2} ({3})", 98, 23, adder(98, 23), (98 + 23));
}
